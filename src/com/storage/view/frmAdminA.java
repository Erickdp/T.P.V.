    private void loadTable() {
        Object[] columns = {"Job", "First Name", "Last Name", "Password", "E-mail"};
        DefaultTableModel model = new DefaultTableModel(columns, 0);
        for (Admin a : admins) {
            Object[] rows = {a.getJob(), a.getFirstName(),
                a.getLastName(), a.getPassword(), a.getEmail()};
            model.addRow(rows);
        }
        this.rSTableMetro2.setModel(model);
    }

