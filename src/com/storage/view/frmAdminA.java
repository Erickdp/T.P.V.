    private void rSButtonRiple1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSButtonRiple1ActionPerformed
        // TODO add your handling code here:
        if (!(jJob.getText().isEmpty() | jFirst.getText().isEmpty() | jLast.getText().isEmpty()
                | jPass.getText().isEmpty() | jEmail.getText().isEmpty())) {
            try {
                atrs.create(new Admin(jJob.getText(), jFirst.getText(),
                        jLast.getText(), jPass.getText(), jEmail.getText()));
                jEmail.setText(null);
                jJob.setText(null);
                jLast.setText(null);
                jFirst.setText(null);
                jPass.setText(null);
            } catch (MyException ex) {
                new ErrorMessage(new Frame(), true, true, ex.getMessage()).setVisible(true);
            } finally {
                loadTable();
            }
        } else {
            new ErrorMessage(new Frame(), true, true, "Please, complete all fields.").setVisible(true);
        }
    }//GEN-LAST:event_rSButtonRiple1ActionPerformed

    private void rSButtonRiple2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSButtonRiple2ActionPerformed
        // TODO add your handling code here:
        if (!(jJob.getText().isEmpty() | jEmail.getText().isEmpty())) {
            try {
                atrs.update(new Admin(jJob.getText(), atrs.searchAdminByEmail(jEmail.getText()).getFirstName(),
                        atrs.searchAdminByEmail(jEmail.getText()).getLastName(), atrs.searchAdminByEmail(jEmail.getText()).getPassword(),
                        jEmail.getText()));
                jEmail.setText(null);
                jJob.setText(null);
            } catch (MyException ex) {
                new ErrorMessage(new Frame(), true, true, ex.getMessage()).setVisible(true);
            } finally {
                loadTable();
            }
        } else if (!(jEmail.getText().isEmpty() | jPass.getText().isEmpty())) {
            try {
                atrs.update(new Admin(atrs.searchAdminByEmail(jEmail.getText()).getJob(), atrs.searchAdminByEmail(jEmail.getText()).getFirstName(),
                        atrs.searchAdminByEmail(jEmail.getText()).getLastName(), jPass.getText(),
                        atrs.searchAdminByEmail(jEmail.getText()).getEmail()));
                jEmail.setText(null);
                jJob.setText(null);
            } catch (MyException ex) {
                new ErrorMessage(new Frame(), true, true, ex.getMessage()).setVisible(true);
            } finally {
                loadTable();
            }
        } else {
            new ErrorMessage(new Frame(), true, true, "Please, complete all fields.").setVisible(true);
        }
    }//GEN-LAST:event_rSButtonRiple2ActionPerformed

