    private void jTextField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER & !(jTextField1.getText().isEmpty())) {
            try {
                this.dispose();
                new ErrorMessage(new Frame(), true, false, Useful.clientRegistred.getStoreCard().transfer(Double.parseDouble(jTextField1.getText()),
                        "DEPOSIT")).setVisible(true);
                ClientTrs ctrs = new ClientTrs();
                ctrs.read();
                ctrs.update(Useful.clientRegistred);
            } catch (MyException ex) {
                new ErrorMessage(new Frame(), true, true, ex.getMessage()).setVisible(true);
            }
        }
    }//GEN-LAST:event_jTextField1KeyPressed

